    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        MauSac ms = new MauSac();
        ms.setMa(txtMa.getText());
        ms.setTen(txtTen.getText());
        ms.setTrangthai(Integer.parseInt(txtTrangThai.getText()));
//        ms.setNgayTao(txtNgayTao.getText());
//        ms.setNgaySua(txtNgaySua.getText());
        JOptionPane.showMessageDialog(this, mauSacService.addSinhVien(ms));
        loadTable(mauSacService.getListSinhVien());
    }//GEN-LAST:event_btnThemActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int index = tblMauSac.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Yêu cầu chọn thông tin trong bảng");
            return;
        } else {
            int row = tblMauSac.getSelectedRow();
            MauSac ms = mauSacService.getListSinhVien().get(row);
            ms.setMa(txtMa.getText());
            ms.setTen(txtTen.getText());
            ms.setTrangthai(Integer.parseInt(txtTrangThai.getText()));
            mauSacService.Update(ms);
            loadTable(mauSacService.getListSinhVien());
            JOptionPane.showMessageDialog(this, "Sửa thành công!");
        }
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        txtID.setText(null);
        txtMa.setText(null);
        txtTrangThai.setText(null);
        txtTen.setText(null);
    }//GEN-LAST:event_btnResetActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int index = tblMauSac.getSelectedRow();
        if (index < 0) {
            JOptionPane.showMessageDialog(this, "Yêu cầu chọn thông tin trong bảng");
            return;
        } else {
            String ma = (txtMa.getText());
            mauSacService.delete(ma);
            loadTable(mauSacService.getListSinhVien());
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void tblMauSacMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMauSacMouseClicked
        int row = tblMauSac.getSelectedRow();
//        moseMS(row);
        txtID.setText(tblMauSac.getValueAt(row, 0).toString());
        txtMa.setText(tblMauSac.getValueAt(row, 1).toString());
        txtTen.setText(tblMauSac.getValueAt(row, 2).toString());
        txtTrangThai.setText(tblMauSac.getValueAt(row, 3).toString());
    }//GEN-LAST:event_tblMauSacMouseClicked

