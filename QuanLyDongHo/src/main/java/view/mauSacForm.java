    private void loadTable(List<MauSac> list) {
        tblModel = (DefaultTableModel) tblMauSac.getModel();
        tblModel.setRowCount(0);
        for (MauSac ms : list) {
            tblModel.addRow(new Object[]{
                ms.getId(), ms.getMa(), ms.getTen(), ms.getTrangthai()
            });
        }
    }

    private void listLoad() {
        list = mauSacService.getListSinhVien();
        loadTable(mauSacService.getListSinhVien());
    }

